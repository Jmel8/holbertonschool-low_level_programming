#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - suma two numbers
 * @a: number 1
 * @b: number 2
 * Return: suma
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract two numbers
 * @a: number 1
 * @b: number 2
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two numbers
 * @a: number 1
 * @b: number 2
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numbers
 * @a: number 1
 * @b: number 2
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - the remainder of two numbers
 * @a: number 1
 * @b: number 2
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
